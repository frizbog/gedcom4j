package org.gedcom4j.model;

/**
 * A User reference.
 * 
 * @author frizbog1
 */
public class UserReference extends AbstractElement {
    /**
     * The reference number
     */
    public String referenceNum;

    /**
     * The type of reference
     */
    public String type;

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (!super.equals(obj)) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        UserReference other = (UserReference) obj;
        if (referenceNum == null) {
            if (other.referenceNum != null) {
                return false;
            }
        } else if (!referenceNum.equals(other.referenceNum)) {
            return false;
        }
        if (type == null) {
            if (other.type != null) {
                return false;
            }
        } else if (!type.equals(other.type)) {
            return false;
        }
        return true;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = super.hashCode();
        result = prime * result + (referenceNum == null ? 0 : referenceNum.hashCode());
        result = prime * result + (type == null ? 0 : type.hashCode());
        return result;
    }

    @Override
    public String toString() {
        return "UserReference [" + (referenceNum != null ? "referenceNum=" + referenceNum + ", " : "") + (type != null ? "type=" + type + ", " : "")
                + (customTags != null ? "customTags=" + customTags : "") + "]";
    }
}
